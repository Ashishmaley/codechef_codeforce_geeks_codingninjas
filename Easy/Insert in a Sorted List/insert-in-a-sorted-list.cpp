class Solution{
  public:
    Node *sortedInsert(struct Node* head, int data) {
        
        Node* newHead = new Node(data);
        if (head == nullptr || head->data >= data) {
           newHead->next = head;
           return newHead;
        }
        
        Node* curr = head;
        
        while (curr->next != nullptr && curr->next->data < data) {
            curr = curr->next;
        }
        
        Node* temp = curr->next;
        curr->next = newHead;
        newHead->next = temp;
        
        return head;
    }
};


